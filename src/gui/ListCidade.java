    private void iniciarBD() {

        try {
            alunoDao = new AlunoDao();
            cidadeDao = new CidadeDao();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }

    private void montarTabela() {
        try {
            ArrayList<Cidade> cidades = cidadeDao.ListarCidade();

            int linha = 0;

            for (Cidade cidade : cidades) {
                int cod = cidade.getCod();
                String nome = cidade.getNomeCidade();
                String estado = cidade.getEstado();
                tbCidade.getModel().setValueAt(cod, linha, 0);
                tbCidade.getModel().setValueAt(nome, linha, 1);
                tbCidade.getModel().setValueAt(estado, linha, 2);
                linha++;
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }
    }
